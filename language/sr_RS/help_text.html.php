<h4>Prenos fajlova manjih od 2 gigabajta (2GB) uz Adobe Flash</h4>
<ul>
        <li>Ako možete da gledate YouTube filmove ova metoda bi trebala da funkcioniše</li>
        <li>Treba vam noviji pretraživač sa verzijom 10 (ili višom) <a target="_blank" href="http://www.adobe.com/software/flash/about/">Adobe Flash-a.</a></li>
        <li>FileSender će vas upozoriti ako budete pokušali da prenesete fajl koji je preveliki za prenos ovom metodom</li>
</ul>

<h4>Prenos fajlova <i>bilo koje veličine</i> uz HTML5</h4>
<ul>
        <li>Ako vidite <img src="images/html5_installed.png" alt="green HTML5 tick" class="textmiddle" style="display:inline" /> u FileSender-ovom gornjem desnom uglu ova metoda će vam funkcionisati</li>
        <li>Treba vam novija verzija pretraživača koji podržava HTML5, zadnju verziju "jezika web-a".</li>
        <li>Trenutno Firefox4 (i više verzije) i Chrome na Windows-ima, Mac OSX i Linux-ima rade.</li>
        <li>Molimo vas da koristite <a href="http://caniuse.com/fileapi" target="_blank">"Kada mogu koristiti..."</a> web stranicu za praćenje implementacije HTML5 FileAPI za sve vodeće pretraživače.  Posebno podršku za <a href="http://caniuse.com/filereader" target="_blank">FileReader API</a> i <a href="http://caniuse.com/bloburls" target="_blank">Blob URLs</a> moraju biti označeni zeleno (=podržano) za pretraživače koji podržavaju prenos fajlova većih od 2 GB </li>
</ul>

<h4>Preuzimanje fajlova bilo koje veličine</h4>
<ul>
        <li>Treba vam noviji pretraživač, Adobe Flash ili HTML5 <b>nisu</b> neophodni za preuzimanje</li>
</ul>


