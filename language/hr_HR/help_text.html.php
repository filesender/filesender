<h4>Prijenos datoteka manjih od 2 gigabajta (2GB) uz Adobe Flash</h4>
<ul>
        <li>Ako možete gladati YouTube filmove ova bi vam metoda trebala funkcionirati</li>
        <li>Trebate moderan preglednik sa verzijom 10 (ili višom) <a target="_blank" href="http://www.adobe.com/software/flash/about/">Adobe Flash-a.</a></li>
        <li>FileSender će  vas upozoriti budete li pokušali prenijeti datoteku koja je prevelika za prenijeti ovom metodom</li>
</ul>

<h4>Prijenos datoteka <i>bilo koje veličine</i> uz HTML5</h4>
<ul>
        <li>Ako vidite <img src="images/html5_installed.png" alt="green HTML5 tick" class="textmiddle" style="display:inline" /> u FileSender-ovom gornjem desnom uglu ova metoda će vam funkcionirati</li>
        <li>Trebate novije verzije preglednika koji podržavaju HTML5, zadnju verziju "jezika web-a".</li>
        <li>Trenutno Firefox4 (i više verzije) i Chrome na Windows-ima, Mac OSX i Linux-ima rade.</li>
        <li>Molimo vas da koristite <a href="http://caniuse.com/fileapi" target="_blank">"Kada mogu koristiti..."</a> web stranicu za praćenje implementacije HTML5 FileAPI za sve vodeće preglednike.  Posebno podršku za <a href="http://caniuse.com/filereader" target="_blank">FileReader API</a> i <a href="http://caniuse.com/bloburls" target="_blank">Blob URLs</a> moraju biti označeni zeleno (=podržano) za preglednike koji podržavaju prijenos datoteka većih od 2 GB </li>
</ul>

<h4>Preuzimanje datoteka bilo koje veličina</h4>
<ul>
        <li>Trebate moderan preglednik, Adobe Flash ili HTML5 <b>nisu</b> nužni za preuzimanje</li>
</ul>


<h4>Ograničenja FileSender instalacije</h4>
<ul>
    <li><strong>
      Maximalan broj primatelja: </strong>{cfg:max_email_recipients} primatelja (adrese odvojite točka-zarezom)</li>
    <li><strong>Maksimalan broj datoteka po prijenosu:</strong> jedna - za prijenos više datoteka odjednom, najprije ih zapakirajte u arhivu</li>
    <li><strong>Maksimalna veličina datoteke po prijenosu, sa Adobe Flash-om samo: </strong>{size:cfg:max_legacy_file_size} </li>
    <li><strong>Maksimalna veličina datoteke po prijenosu, sa HTML5: </strong>{size:cfg:max_html5_upload_size}</li>
    <li><strong>Maksimalan broj dana nakon kojeg datoteke/kuponi istuču: </strong>{cfg:default_transfer_days_valid} </li>
</ul>
